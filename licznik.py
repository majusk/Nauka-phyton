# Program Licznik
# Demonstruje funkcje range()

print("liczenie:")
for i in range(10):
    print(i, end=" ")

print("\n\nLiczenie co pięć:")
for i in range(0, 50, 5):
    print(i, end=" ")

print("\n\nLiczenie do tyłu:")
for i in range(10, 0, -1):
    print(i, end=" ")

